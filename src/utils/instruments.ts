export function getBVCStocks(): Instrument[] {
  return ALL_INSTRUMENTS.filter(i => i.category === 'colombia_stock');
}

/**
 * Obtiene todos los instrumentos colombianos (ETFs + acciones BVC)
 */
export function getColombianInstruments(): Instrument[] {
  return ALL_INSTRUMENTS.filter(i => i.market === 'colombia');
}

/**
 * Busca un instrumento por ID
 */
export function getInstrumentById(id: string): Instrument | undefined {
  return ALL_INSTRUMENTS.find(i => i.id === id);
}

/**
 * Busca un instrumento por ticker
 */
export function getInstrumentByTicker(ticker: string): Instrument | undefined {
  return ALL_INSTRUMENTS.find(i => i.ticker.toUpperCase() === ticker.toUpperCase());
}

